namespace HandyMath
{
  /// <summary>
  /// Basic operations one might do on simple numeric types like integers.
  /// </summary>
  public static class Basic

  {
    /// <summary>
    /// Add the supplied numbers.
    /// </summary>
    /// <param name="left">The left argument.</param>
    /// <param name="right">The right argument.</param>
    /// <returns>The sum of the supplied numbers.</returns>
    public static long Add(long left, long right)
    {
      return left + right;
    }
  }
}
